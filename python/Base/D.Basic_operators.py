#-------------------opérateurs arithmétiques-------------------

# Adition
addition = 2 + 5
# Soustraction
subtraction = 50 - 6
# Multiplication
multiplication = 5 * 5
# Division (donne un float)
division = 92 / 3
# Reste de division (donne un entier)
modulo = 92 % 3
# calculé au carré
squared = 7 ** 2
# calculé au cube
cubed = 2 ** 3
# calculé au n (ici vaut 15)
ned = 3 ** 15


